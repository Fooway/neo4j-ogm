package org.springframework.data.neo4j.ogm.domain.cineasts.annotated;

import org.springframework.data.neo4j.ogm.annotation.EndNode;
import org.springframework.data.neo4j.ogm.annotation.GraphId;
import org.springframework.data.neo4j.ogm.annotation.RelationshipEntity;
import org.springframework.data.neo4j.ogm.annotation.StartNode;

@RelationshipEntity(type = "RATED")
public class Rating {

    @GraphId
    private Long id;
    @StartNode
    private User user;
    @EndNode
    private Movie movie;
    private int stars;
    private String comment;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Movie getMovie() {
        return movie;
    }

    public void setMovie(Movie movie) {
        this.movie = movie;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
